n = int(input())

gp = [0]*(n+1)

for i in range(2,n+1):
    if i%3 == 0 and i%2 == 0:
        gp[i] = min(gp[i//3],gp[i//2],gp[i-1]) + 1
    elif i%3 == 0:
        gp[i] = min(gp[i//3],gp[i-1]) + 1
    elif i%2 == 0:
        gp[i] = min(gp[i//2],gp[i-1])+1
    else:
        gp[i] = gp[i-1]+1
print(gp[n])
        
    


